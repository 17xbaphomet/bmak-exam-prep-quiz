#!/usr/bin/env node
/**
 * Scan questions/ for subject modules and generate questions/manifest.js
 *
 * Subject dirs:  /^[A-Z]{2,4}-[a-zA-Z_]+$/
 * Question files: /^[0-9]+[a-zA-Z-]*\.js$/
 *
 * Usage: node scripts/generate-manifest.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const QDIR = path.join(ROOT, 'questions');
const SUBJECT_RE = /^[A-Z]{2,4}-[a-zA-Z_]+$/;
const FILE_RE = /^[0-9]+[a-zA-Z-]*\.js$/;

function scan() {
  if (!fs.existsSync(QDIR)) {
    console.error('questions/ directory not found');
    process.exit(1);
  }

  const subjects = {};
  const entries = fs.readdirSync(QDIR, { withFileTypes: true });

  for (const ent of entries) {
    if (!ent.isDirectory()) continue;
    if (!SUBJECT_RE.test(ent.name)) {
      console.warn(`Skipping non-subject dir: ${ent.name}`);
      continue;
    }
    const subdir = path.join(QDIR, ent.name);
    const files = fs.readdirSync(subdir)
      .filter(f => FILE_RE.test(f))
      .sort((a, b) => {
        const na = parseInt(a, 10) || 0;
        const nb = parseInt(b, 10) || 0;
        return na - nb || a.localeCompare(b);
      });
    subjects[ent.name] = files;
    console.log(`${ent.name}: ${files.length} file(s)`);
  }

  return subjects;
}

function writeManifest(subjects) {
  const keys = Object.keys(subjects).sort();
  const payload = {
    generated: new Date().toISOString(),
    subjects: keys.map(id => ({
      id,
      label: id.replace(/-/g, ' · '),
      files: subjects[id]
    }))
  };

  const js = `/* Auto-generated by scripts/generate-manifest.js – do not edit by hand */
window.QUESTION_MANIFEST = ${JSON.stringify(payload, null, 2)};
`;

  const out = path.join(QDIR, 'manifest.js');
  fs.writeFileSync(out, js, 'utf8');
  console.log(`Wrote ${out} (${keys.length} subject(s))`);
}

const subjects = scan();
if (Object.keys(subjects).length === 0) {
  console.error('No subject modules found matching /^[A-Z]{2,4}-[a-zA-Z_]+$/');
  process.exit(1);
}
writeManifest(subjects);
